# @lc code=start
class Solution(object):
    def subsets(self, nums):
        """
        :type nums: List[int]
        :rtype: List[List[int]]
        """
        res = [[]]
        for num in nums:
            tmp = res[:]
            for sub in res:
                tmp.append(sub + [num])
            res = tmp
        return res
# @lc code=end

